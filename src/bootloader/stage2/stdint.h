#pragma once

/**
 * @brief signed 1 byte wide interger
 */
typedef signed char int8_t;
/**
 * @brief unsigned 1 byte wide interger
 */
typedef unsigned char uint8_t;
/**
 * @brief signed 2 byte wide interger
 */
typedef short int int16_t;
/**
 * @brief unsigned 2 byte wide interger
 */
typedef unsigned short int uint16_t;
/**
 * @brief signed 4 byte wide interger
 */
typedef int int32_t;
/**
 * @brief unsigned 4 byte wide interger
 */
typedef unsigned int uint32_t;
/**
 * @brief signed 8 byte wide interger
 */
typedef long long int int64_t;
/**
 * @brief unsigned 8 byte wide interger
 */
typedef unsigned long long int uint64_t;